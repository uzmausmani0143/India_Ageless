                                                <p>The skills used to manufacture the perfume have been passed down through successive generations. In describing this process, one artisan said, "My family has been working in this field since three centuries and my son is the 30th generation".</p>

                                                <p>The perfume is made from flowers and natural resources. <b>Musk</b>, <b>camphor</b>, <b>saffron</b> and other aromatic substances are also used in its production. Flowers like <b>white jasmine</b> and plants such as <b>vetiver</b> are used for summer varieties, while soil is used for a monsoon variety, called <b>Mitti attar</b>. The smell of Mitti attar resembles petrichor, the loamy smell of a first rain. Heena attar and musk attar are winter varieties.</p>

                                                <p>The perfume does not usually contain alcohol or other chemicals. Perfume made from rose is more potent, while attar made from sandalwood oil lasts for a long time. The typical creation process of a small bottle takes roughly 15 days.
